    }
    
    static func gloryExtraLightItalic(with size:CGFloat)->Font {
        return .custom("Glory-ExtraLightItalic", size: size)
    }
    
    static func gloryRegularItalic(with size:CGFloat)->Font {
        return .custom("Glory-Italic", size: size)
    }
    
    static func gloryLightItalic(with size:CGFloat)->Font {
        return .custom("Glory-LightItalic", size: size)
    }
    
    static func gloryLight(with size:CGFloat)->Font {
        return .custom("Glory-Light", size: size)
